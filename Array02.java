
//write a java program by using for each loop


class Array02 
{
	public static void main(String[] args) 
	{
		char[] f = new char[3];
		for(char ff : f)
			{
				System.out.println(ff);
			}
			float[] l = new float[3];
		for(float ll : l)
			{
				System.out.println(ll);
			}
			boolean[] s = new boolean[3];
		for(boolean ss : s)
			{
				System.out.println(ss);
			}
	}
}
